# R(10^1), R(10^2), R(10^3) は 17で割り切れないが R(10^4) は割り切れる
# R(n) = (10^n-1)/9
#
# R(n) が素数 p で割り切れる <=> R(n) = 0 mod p <=>
# (10^n - 1) / 9 = 0 mod p <=> 10^n - 1 = 0 mod 9p <=>
# 10^n = 1 mod 9p
#
# よって,
# R(n) が素数 p で割り切れない <=> 任意の n>=1 に対し, 10^n != 1 mod 9p
#
# 問題文の 17, 41, 73 で確かめると,
# 10^10000 = 1 mod 17
# 10^10 = 1 mod 41
# 10^1000 = 1 mod 73
